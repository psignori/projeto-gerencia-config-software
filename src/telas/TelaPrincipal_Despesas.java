    private void botaoCadastrarREActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarREActionPerformed
        despesasDAO deDAO = new despesasDAO();
        despesas de = new despesas();
        Boolean passou = false;
                
        if (this.campoNome.getText() == "" | this.campovalor.getText() == "" | this.campoDesc.getText() == ""){
            passou = false;
        } else {
            passou = true;
        }     
        if (passou == true){
            try {
               de.setNome(this.campoNome.getText());
               double x = (Double.parseDouble(this.campovalor.getText()));
               de.setValor(x);
               de.setDescricao(this.campoDesc.getText());
               deDAO.salvar(de, this.id);
               JOptionPane.showMessageDialog(null, "Registro cadastrado com sucesso!");
               this.campoNome.setText("");
               this.campovalor.setText("");
               this.campoDesc.setText("");
               new despesasDAO().popularTabela_tela_principal2(TableDespesas, id);
            } catch (Exception e){
              JOptionPane.showMessageDialog(null, "Problemas ao cadastrar registro, verifique os dados inseridos!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Prencha todos os campos antes de realizar um novo cadastro!");
        }
    }//GEN-LAST:event_botaoCadastrarREActionPerformed

