    private void botaoModificarREActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoModificarREActionPerformed
        despesasDAO deDAO = new despesasDAO();
        despesas de = new despesas();
        
        String identifi;
        int coluna = 0;
        int codigo;
        
        if (botaoModificarRE.getText() == "Modificar"){
                            
            

        
            if (this.teste != 0){
                identifi = TableDespesas.getValueAt(TableDespesas.getSelectedRow(), coluna).toString();
                int iden = Integer.parseInt(identifi);
                if (identifi != null){
                    codigo = Integer.parseInt(identifi);
                    this.atua = codigo;
                    de = deDAO.consultar(codigo);
                    this.campoNome.setText(de.getNome());
                    this.campovalor.setText(""+de.getValor());
                    this.campoDesc.setText(de.getDescricao());
                    botaoModificarRE.setText("Atualizar");
                } else {
                    JOptionPane.showMessageDialog(null, "Selecione um registro!");
                }
            
                /*ModificarReceitas mr = new ModificarReceitas(re, codigo);
                mr.setVisible(true);*/
            }
        } else {
            de.setNome(this.campoNome.getText());
            double x = (Double.parseDouble(this.campovalor.getText()));
            de.setValor(x);
            de.setDescricao(this.campoDesc.getText());
        
            try {
                deDAO.atualizarCadastro(de, this.atua);
                JOptionPane.showMessageDialog(null, "Registro atualizado com sucesso!");
                botaoModificarRE.setText("Modificar");
                this.campoNome.setText("");
                this.campovalor.setText("");
                this.campoDesc.setText("");
                new despesasDAO().popularTabela_tela_principal2(TableDespesas, this.id);
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Problemas ao salvar registro, verifique os dados inseridos!");
            }
        }
                
    }//GEN-LAST:event_botaoModificarREActionPerformed

